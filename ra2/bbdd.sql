-- Creación de la base de datos si no existe
CREATE DATABASE IF NOT EXISTS mi_base_de_datos;
USE mi_base_de_datos;

-- Creación de la tabla OFICINAS
CREATE TABLE OFICINAS (
    COD INT PRIMARY KEY,
    CIUDAD VARCHAR(100),
    PRESUPUESTO DECIMAL(10,2),
    NUM_PUESTOS INT,
    TIPO ENUM('INTERNA', 'EXTERNA') NOT NULL DEFAULT 'INTERNA'
);

-- Creación de la tabla EMPLEADOS
CREATE TABLE EMPLEADOS (
    COD INT PRIMARY KEY,
    NOMBRE VARCHAR(100),
    DIRECCION VARCHAR(255),
    COD_JEFE INT,
    COD_OFICINA INT,
    FOREIGN KEY (COD_JEFE) REFERENCES EMPLEADOS(COD),
    FOREIGN KEY (COD_OFICINA) REFERENCES OFICINAS(COD)
);

-- Creación de la tabla CLIENTES
CREATE TABLE CLIENTES (
    COD INT PRIMARY KEY,
    NOMBRE VARCHAR(100),
    DIRECCION VARCHAR(255)
);

-- Creación de la tabla PEDIDOS
CREATE TABLE PEDIDOS (
    COD INT PRIMARY KEY,
    FECHA DATE,
    COD_CLIENTE INT,
    FOREIGN KEY (COD_CLIENTE) REFERENCES CLIENTES(COD)
);

-- Creación de la tabla PRODUCTOS
CREATE TABLE PRODUCTOS (
    COD INT PRIMARY KEY,
    NOMBRE VARCHAR(100),
    PVP DECIMAL(10,2),
    STOCK INT
);

-- Creación de la tabla PEDIDOS_PRODUCTOS
CREATE TABLE PEDIDOS_PRODUCTOS (
    COD_PEDIDO INT,
    COD_PRODUCTO INT,
    UNIDADES INT,
    FOREIGN KEY (COD_PEDIDO) REFERENCES PEDIDOS(COD),
    FOREIGN KEY (COD_PRODUCTO) REFERENCES PRODUCTOS(COD),
    PRIMARY KEY (COD_PEDIDO, COD_PRODUCTO)
);

